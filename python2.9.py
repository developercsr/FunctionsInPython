#math module
import math
print(math.ceil(7.65)) 
print(math.floor(6.77))
print(math.fabs(-86.878))
print(math.log(2,10)) #log 2 base 10
print(math.log10(2))
print(math.pow(2,3))#2**3
print(math.sqrt(4)) #4**0.5
print(math.pi) #pi is a constant value
print(math.cos(math.pi))
print(math.sin(math.pi/2))
print(math.tan(math.pi/4))
print(math.acos(-1)) # front a means inverse
print(math.atan(1))
print(math.asin(0))
print(math.degrees(math.pi))
print(math.radians(90))

